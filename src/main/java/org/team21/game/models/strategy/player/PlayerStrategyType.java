package org.team21.game.models.strategy.player;

/**
 * Enum class representing different types of player strategies.
 * This enum defines various player strategy types.
 * Each strategy type corresponds to a specific strategy implementation.
 *
 * @author Meet Boghani
 */
public enum PlayerStrategyType {

    /**
     * Represents the Human Strategy Type.
     */
    HUMAN,
    /**
     * Represents the Aggressive Player Strategy Type.
     */
    AGGRESSIVE,
    /**
     * Represents the Benevolent Player Strategy Type.
     */
    BENEVOLENT,
    /**
     * Represents the Random Player Strategy Type.
     */
    RANDOM,
    /**
     * Represents the Cheater Player Strategy Type.
     */
    CHEATER;

}


